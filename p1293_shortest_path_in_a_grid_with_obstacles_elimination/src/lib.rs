use std::collections::{HashSet, VecDeque};

struct Solution;
impl Solution {
    pub fn shortest_path(grid: Vec<Vec<i32>>, k: i32) -> i32 {
        fn is_valid(row: i32, col: i32, m: i32, n: i32) -> bool {
            return 0 <= row && 0 <= col && row <= m && col <= n;
        }
        let m = (grid.len() - 1) as i32;
        let n = (grid[0].len() - 1) as i32;
        let dir = vec![(0, 1), (1, 0), (0, -1), (-1, 0)];
        let mut q = VecDeque::from([(0, 0, 0, k)]);
        let mut seen = HashSet::from([(0, 0, k)]);
        while let Some((row, col, steps, remain)) = q.pop_front() {
            if row == m && col == n {
                return steps;
            }
            for (dx, dy) in &dir {
                let (next_row, next_col) = (row + dy, col + dx);
                if is_valid(next_row, next_col, m, n) {
                    if grid[next_row as usize][next_col as usize] == 0
                        && !seen.contains(&(next_row, next_col, remain))
                    {
                        seen.insert((next_row, next_col, remain));
                        q.push_back((next_row, next_col, steps + 1, remain));
                    } else if remain > 0 && !seen.contains(&(next_row, next_col, remain - 1)) {
                        seen.insert((next_row, next_col, remain - 1));
                        q.push_back((next_row, next_col, steps + 1, remain - 1));
                    }
                }
            }
        }
        -1
    }
}
